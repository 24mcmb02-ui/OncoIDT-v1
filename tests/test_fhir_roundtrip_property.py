def test_observation_missing_loinc_adds_quality_flag() -> None:
    resource = {
        "resourceType": "Observation",
        "id": "obs-001",
        "status": "final",
        "code": {"coding": [{"system": "http://snomed.info/sct", "code": "12345"}]},
        "subject": {"reference": "Patient/p-001"},
        "effectiveDateTime": "2026-01-01T00:00:00+00:00",
        "valueQuantity": {"value": 37.2, "unit": "Cel"},
    }
    records = adapter.parse(json.dumps(resource).encode())
    assert len(records) == 1
    flags = records[0].data_quality_flags
    assert any(f.field_name == "loinc_code" for f in flags)


def test_bundle_parses_multiple_resources() -> None:
    bundle = {
        "resourceType": "Bundle",
        "type": "collection",
        "entry": [
            {"resource": {
                "resourceType": "Observation",
                "id": "obs-1",
                "status": "final",
                "code": {"coding": [{"system": "http://loinc.org", "code": "8310-5"}]},
                "subject": {"reference": "Patient/p-1"},
                "effectiveDateTime": "2026-01-01T00:00:00+00:00",
                "valueQuantity": {"value": 37.5, "unit": "Cel"},
            }},
            {"resource": {
                "resourceType": "MedicationAdministration",
                "id": "med-1",
                "status": "completed",
                "medicationCodeableConcept": {
                    "coding": [{"system": "http://www.nlm.nih.gov/research/umls/rxnorm", "code": "4492"}],
                    "text": "Cyclophosphamide",
                },
                "subject": {"reference": "Patient/p-1"},
                "effectiveDateTime": "2026-01-01T01:00:00+00:00",
                "dosage": {"dose": {"value": 500.0, "unit": "mg"}, "route": {"text": "IV"}},
            }},
        ],
    }
    records = adapter.parse(json.dumps(bundle).encode())
    assert len(records) == 2
    types = {r.record_type for r in records}
    assert "vital" in types or "lab" in types
    assert "medication" in types


def test_invalid_json_raises_422() -> None:
    from fastapi import HTTPException
    with pytest.raises(HTTPException) as exc_info:
        adapter.parse(b"not json at all")
    assert exc_info.value.status_code == 422


def test_missing_effective_datetime_raises_422() -> None:
    from fastapi import HTTPException
    resource = {
        "resourceType": "Observation",
        "id": "obs-bad",
        "status": "final",
        "code": {"coding": [{"system": "http://loinc.org", "code": "8310-5"}]},
        "subject": {"reference": "Patient/p-1"},
        # no effectiveDateTime
        "valueQuantity": {"value": 37.0, "unit": "Cel"},
    }
    with pytest.raises(HTTPException) as exc_info:
        adapter.parse(json.dumps(resource).encode())
    assert exc_info.value.status_code == 422
